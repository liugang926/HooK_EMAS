<template>
  <router-view />
</template>

<script setup>
import { onMounted } from 'vue'
import { useUserStore } from '@/stores/user'

const userStore = useUserStore()

onMounted(() => {
  // 检查登录状态
  const token = localStorage.getItem('access_token')
  if (token) {
    userStore.checkAuth()
  }
})
</script>

